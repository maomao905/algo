"""
[0,0,1,2,2,5,6]
[2,5,6,0,0,1,2] target=0
l r mid
0 6 3  -> nums[mid] = target

[2,5,6,0,0,1,2] target=3
l r mid
0 6 3  nums[mid] = 0 < 3  nums[left] >= nums[mid] -> pivot in left, nums[mid] < target < nums[right] is False, go left
0 2 1  nums[mid] = 5 > 3  nums[left] < nums[mid] -> pivot in right, nums[left] < target < nums[mid] is True, go left
0 0 0  nums[mid] = 2 < 3  nums[left] == nums[mid]

--> this does not work when [1,3,1,1,1]
if left is index 0 and right is index 5 and mid is index 2, 
even though pivot exists in left but we cannot determine that based on left, mid, right (all elements are 1)
so, above solution only works when there is no duplicate.

so, just move the left and right pointer to left and right by one

time: O(N) in worst O(logN) in best
space: O(1)
"""

from typing import List

class Solution:
    def search(self, nums: List[int], target: int) -> bool:
        N=len(nums)
        l, r = 0, N-1
        while l <= r:
            mid = l + (r-l)//2
            if nums[mid] == target:
                return True
            
            if l == r:
                return False
            
            if nums[l] == nums[mid] == nums[r]:
                if nums[l] == target:
                    return True
                else:
                    l += 1
                    r -= 1
                    continue
            
            is_pivot_left = nums[l] > nums[mid]
            
            if is_pivot_left:
                if nums[mid] < target <= nums[r]:
                    l = mid+1
                else:
                    r = mid-1
            else:
                if nums[l] <= target < nums[mid]:
                    r = mid-1
                else:
                    l = mid+1
        
        return False

